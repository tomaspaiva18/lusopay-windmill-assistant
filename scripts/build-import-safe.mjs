import fs from 'node:fs';
import path from 'node:path';

const sourceRoot = path.resolve('workflows');
const outputRoot = path.resolve('dist', 'workflows-import');

function walk(directory) {
  return fs.readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const target = path.join(directory, entry.name);
    return entry.isDirectory() ? walk(target) : [target];
  });
}

function cleanWorkflow(workflow) {
  delete workflow.id;
  delete workflow.versionId;
  delete workflow.meta;
  delete workflow.shared;
  delete workflow.tags;

  workflow.active = false;
  workflow.settings ??= {};
  delete workflow.settings.errorWorkflow;

  for (const node of workflow.nodes ?? []) {
    delete node.webhookId;

    if (node.credentials) {
      // Credential IDs are instance-specific. Keep the expected credential
      // name in documentation and let the operator select it after import.
      delete node.credentials;
    }

    const workflowId = node.parameters?.workflowId;
    if (workflowId && typeof workflowId === 'object') {
      // Imported workflow IDs are regenerated by n8n. Retaining a repository
      // ID can make validation fail or leave a misleading broken reference.
      workflowId.value = '';
      workflowId.mode = 'list';
    }
  }

  return workflow;
}

fs.rmSync(outputRoot, { recursive: true, force: true });

for (const file of walk(sourceRoot).filter((file) => file.endsWith('.json'))) {
  const relative = path.relative(sourceRoot, file);
  const output = path.join(outputRoot, relative);
  const workflow = cleanWorkflow(JSON.parse(fs.readFileSync(file, 'utf8')));

  fs.mkdirSync(path.dirname(output), { recursive: true });
  fs.writeFileSync(output, `${JSON.stringify(workflow, null, 2)}\n`, 'utf8');
}

